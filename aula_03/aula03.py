# Operadores Artméticos
"""
Sempre apresentar o resultado após cada etapa finalizada
"""

# queremos somar 3 números
numero1 = 2
numero2 = 4
numero3 = 6

soma = numero1 + numero2 + numero3
print(soma)

# após somar 3 números, vamos subtrair 15
subtracao = soma - 15
print(subtracao)

# após a etapa acima, vamos multiplicar o resultado por 8
multiplicacao = subtracao * 8
print(multiplicacao)

# em seguida dividir o resultado por 100
divisao = multiplicacao / 100
print(divisao) 

"""
Para mostrar o resultado na tela, sempre colocar print()
"""

